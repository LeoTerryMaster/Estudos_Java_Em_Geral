package XTI.src.lp.operadorcomparacao;

public class OperadorDeComapracao{


	public static void main(String[] args){

		int idade = 41;


		System.out.println(idade == 10 );
		System.out.println(idade != 10);
		System.out.println(idade > 10);
		System.out.println(idade < 10);
		System.out.println(idade <= 10);
		System.out.println(idade >= 10);


	}

}
