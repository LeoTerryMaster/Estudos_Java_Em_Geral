package XTI.src.lp.foreachs;

public class Fores{


	public static void main(String[] args){

		for (int i = 0; i <= 10; i++){
			System.out.print(i + ", ");
		}

		System.out.println();

		for (int i = 10; i >= 0; i--){
			System.out.print(i + ", ");
		}

		
		System.out.println();
		int i;
		for (i = 0; i <= 10; i++){
			System.out.print(i + ", ");
		}

	}

}
