package DevDojo.exercicios;

public class Variaveis{

	/*
	 * todos tido pode variável são referencia a dados
	 * todo tipo de variável começa com a letra minuscúla 
	 * e se for nome composto precisa usar o padrão camelcase
	 * a primeira letra da palavra minuscula e a sengunda letra da segunda palavra maiúscula.
	 * */

	public static String nome = "Leandro Gomes";
	public static int salario = 50000;
	public static String endereco = "Rua Regina n300";


	public static void main(String[] args){

		System.out.printf("meu nome é %s salário de %d no endereço %s ", nome, salario, endereco);

	}


}
