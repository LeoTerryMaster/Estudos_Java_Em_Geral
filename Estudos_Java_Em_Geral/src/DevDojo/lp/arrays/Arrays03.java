package DevDojo.lp.arrays;

import java.util.Arrays;

public class Arrays03 {

    public static void main(String[] args) {

        int[] idade = new int[5];
        idade[0] = 10;
        idade[1] = 25;
        idade[2] = 15;
        idade[3] = 5;
        idade[4] = 55;


        for (int i : idade) {
            System.out.println(i);
        }

    }
}
